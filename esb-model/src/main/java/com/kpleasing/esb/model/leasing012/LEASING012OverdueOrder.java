package com.kpleasing.esb.model.leasing012;

public class LEASING012OverdueOrder {
	
	private String applyno;
	
	private String item;
	
	private String repay_date;
	
	private String overdue_amt;
	
	private String days;
	
	private String penalty_amt;
	
	private String cust_id;
	
	private String phone;
	
	private String cust_name;

	public String getApplyno() {
		return applyno;
	}

	public void setApplyno(String applyno) {
		this.applyno = applyno;
	}

	public String getItem() {
		return item;
	}

	public void setItem(String item) {
		this.item = item;
	}

	public String getRepay_date() {
		return repay_date;
	}

	public void setRepay_date(String repay_date) {
		this.repay_date = repay_date;
	}

	public String getOverdue_amt() {
		return overdue_amt;
	}

	public void setOverdue_amt(String overdue_amt) {
		this.overdue_amt = overdue_amt;
	}

	public String getDays() {
		return days;
	}

	public void setDays(String days) {
		this.days = days;
	}

	public String getPenalty_amt() {
		return penalty_amt;
	}

	public void setPenalty_amt(String penalty_amt) {
		this.penalty_amt = penalty_amt;
	}

	public String getCust_id() {
		return cust_id;
	}

	public void setCust_id(String cust_id) {
		this.cust_id = cust_id;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}

	public String getCust_name() {
		return cust_name;
	}

	public void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}
}
