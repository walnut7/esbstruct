package com.kpleasing.esb.model.leasing012;

public class OverdueOrder {
	
	private String applyno;
	
	private String times;
	
	private String due_date;
	
	private String due_amount;
	
	private String overdue_days;
	
	private String penalty_amount;
	
	private String cell_phone;
	
	private String cust_name;
	
	private String cust_id;

	public String getApplyno() {
		return applyno;
	}

	public void setApplyno(String applyno) {
		this.applyno = applyno;
	}

	public String getTimes() {
		return times;
	}

	public void setTimes(String times) {
		this.times = times;
	}

	public String getDue_date() {
		return due_date;
	}

	public void setDue_date(String due_date) {
		this.due_date = due_date;
	}

	public String getDue_amount() {
		return due_amount;
	}

	public void setDue_amount(String due_amount) {
		this.due_amount = due_amount;
	}

	public String getOverdue_days() {
		return overdue_days;
	}

	public void setOverdue_days(String overdue_days) {
		this.overdue_days = overdue_days;
	}

	public String getPenalty_amount() {
		return penalty_amount;
	}

	public void setPenalty_amount(String penalty_amount) {
		this.penalty_amount = penalty_amount;
	}

	public String getCell_phone() {
		return cell_phone;
	}

	public void setCell_phone(String cell_phone) {
		this.cell_phone = cell_phone;
	}

	public String getCust_name() {
		return cust_name;
	}

	public void setCust_name(String cust_name) {
		this.cust_name = cust_name;
	}

	public String getCust_id() {
		return cust_id;
	}

	public void setCust_id(String cust_id) {
		this.cust_id = cust_id;
	}
}
